    private boolean validar(JTextArea txtNota) {
        String[] p = txtNota.getText().split(" |\\n");
        cadena = "";
        boolean sen = true;
        for (int i = 0; i < p.length; i++) {
            if ("-".equals(au.ConversionNotas(p[i]))) {
                sen = false;
                break;
            }
            cadena = cadena + " " + au.ConversionNotas(p[i]);
        }
        return sen;
    }

